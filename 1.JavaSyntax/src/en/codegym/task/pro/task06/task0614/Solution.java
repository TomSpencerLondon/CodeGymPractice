package en.codegym.task.pro.task06.task0614;

/* 
Remembering geometry
*/

public class Solution {
    public static final double pi = 3.14;
    public static double radius = 2.5;
    public static double diameter = 5;

    public static void main(String[] args) {
        System.out.println("The area of the circle is: " + Solution.pi * Solution.radius * Solution.radius);
        System.out.println("The perimeter of the circle is: " + Solution.pi * Solution.diameter);
    }
}
